import {Tile} from './Tile'

interface Position {
    x: number,
    y: number
}

export class Map {
    private MAP_WIDTH = 19;
    private MAP_HEIGHT = 19;
    private currentMap: Tile[][] = [[]]

    constructor( scene ){

        this.createMap()
    }

    private createMap(){
        for( let i = 0; i < this.MAP_WIDTH; i++ ){
            this.currentMap[i] = []
            for( let j = 0; j < this.MAP_HEIGHT; j++ ){
                let tile = new Tile( i, j, mapSetup[i][j] )
                this.currentMap[i][j] = tile
            }
        }
    }

    public getCurrentTile({ x, y }: Position): Tile{
        let currentX =  Math.round( x / 25 ) 
        let currentY =  Math.round( y / 25 ) 

        return this.currentMap[currentX][currentY]
    }

}


